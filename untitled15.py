def clean_text(x):
    if pd.isna(x):
        return None
    s = str(x).strip()
    if s == '' or s.lower() in {'nan', 'none', 'null'}:
        return None
    s = re.sub(r'\.0$', '', s)
    return s

def normalize_text(x):
    s = clean_text(x)
    return s.lower() if s is not None else None

def to_float(x):
    s = clean_text(x)
    if s is None:
        return np.nan
    try:
        return float(s)
    except:
        return np.nan

def sex_to_num(x):
    s = normalize_text(x)
    if s is None:
        return np.nan
    if s in {'m', 'male', 'man', '1'}:
        return 1.0
    if s in {'f', 'female', 'woman', '0', '2'}:
        return 0.0
    return np.nan

def extract_subject_id(mat_path):
    name = os.path.basename(mat_path).replace('.mat', '')
    name = re.sub(r'^ROISignals[_-]?', '', name, flags=re.IGNORECASE)
    return name

def make_id_candidates(x):
    s = clean_text(x)
    if s is None:
        return []
    cands = {s, s.lower(), s.upper()}

    if s.startswith('sub-'):
        base = s[4:]
        cands.update([base, base.lower(), base.upper()])
    else:
        cands.update([f"sub-{s}", f"sub-{s.lower()}", f"sub-{s.upper()}"])

    digits = re.sub(r'[^0-9]', '', s)
    if digits:
        try:
            n = int(digits)
            cands.update({
                str(n), f"{n:02d}", f"{n:03d}", f"{n:04d}",
                f"sub-{n}", f"sub-{n:02d}", f"sub-{n:03d}", f"sub-{n:04d}"
            })
        except:
            pass

    cands.add(s.replace('-', ''))
    return list(cands)

def find_column(df, exact=None, keywords=None):
    exact = exact or []
    keywords = keywords or []

    for c in exact:
        if c in df.columns:
            return c

    lowered = {str(c).lower(): c for c in df.columns}
    for c in exact:
        if c.lower() in lowered:
            return lowered[c.lower()]

    for col in df.columns:
        low = str(col).lower()
        for kw in keywords:
            if kw in low:
                return col
    return None

def get_signal_from_mat(mat_dict):
    if 'ROISignals' in mat_dict and isinstance(mat_dict['ROISignals'], np.ndarray) and mat_dict['ROISignals'].ndim == 2:
        return mat_dict['ROISignals']
    for k, v in mat_dict.items():
        if k.startswith('__'):
            continue
        if isinstance(v, np.ndarray) and v.ndim == 2:
            return v
    return None

def orient_signal(signal):
    signal = np.asarray(signal, dtype=np.float32)
    if signal.ndim != 2:
        return None
    if signal.shape[0] < signal.shape[1]:
        signal = signal.T
    return signal

def normalize_group_value(x):
    s = normalize_text(x)
    if s is None:
        return None

    if s in {'mdd', 'depression', 'major depressive disorder', 'patient', 'case', 'depressed'}:
        return 'MDD'
    if s in {'hc', 'healthy control', 'healthy controls', 'control', 'controls', 'healthy'}:
        return 'HC'
    if s in {'fedn', 'first episode drug naive', 'first-episode drug-naive', 'first episode drug-naive'}:
        return 'FEDN'
    if s in {'rmdd', 'recurrent mdd', 'recurrent depression'}:
        return 'rMDD'

    # soft matching
    if 'healthy' in s or s == 'hc':
        return 'HC'
    if 'recurrent' in s:
        return 'rMDD'
    if 'first' in s and 'episode' in s:
        return 'FEDN'
    if 'mdd' in s or 'depress' in s:
        return 'MDD'

    return None

def infer_group_from_episode_medication(row):
    first_ep = normalize_text(row.get('If first episode?', None))
    on_med = normalize_text(row.get('On medication?', None))

    yes_vals = {'yes', 'y', '1', 'true'}
    no_vals = {'no', 'n', '0', 'false'}

    if first_ep in yes_vals and on_med in no_vals:
        return 'FEDN'
    return None

def normalize_site_value(x):
    s = clean_text(x)
    if s is None:
        return None
    digits = re.findall(r'\d+', s)
    if digits:
        return str(int(digits[0]))
    return s

def sample_to_target(df, target_mdd, target_hc, random_state=42):
    mdd_df = df[df['group_final'] == 'MDD'].copy()
    hc_df = df[df['group_final'] == 'HC'].copy()

    if len(mdd_df) < target_mdd or len(hc_df) < target_hc:
        raise ValueError(
            f"Not enough subjects to match target counts. "
            f"Found MDD={len(mdd_df)}, HC={len(hc_df)}, need MDD={target_mdd}, HC={target_hc}"
        )

    mdd_keep = mdd_df.sample(n=target_mdd, random_state=random_state)
    hc_keep = hc_df.sample(n=target_hc, random_state=random_state)
    out = pd.concat([mdd_keep, hc_keep], axis=0).sort_values('subject_id').reset_index(drop=True)
    return out

