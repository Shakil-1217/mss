def compute_graph_adj(corr_mat, method='knn_shrinkage', k=10):
    if method == 'knn_shrinkage':
        abs_corr = np.abs(corr_mat)
        np.fill_diagonal(abs_corr, 0.0)
        k_eff = min(k, corr_mat.shape[0] - 1)
        knn = kneighbors_graph(abs_corr, n_neighbors=k_eff, mode='connectivity',
                               include_self=False)
        knn_arr = knn.toarray()
        adj_mask = np.maximum(knn_arr, knn_arr.T)
        weighted_adj = corr_mat * adj_mask
        np.fill_diagonal(weighted_adj, 0.0)
        weighted_adj = np.nan_to_num(weighted_adj, nan=0.0, posinf=0.0, neginf=0.0)
        return weighted_adj.astype(np.float32)
    else:
        raise ValueError(f"Unknown method: {method}")


def build_graphs_iga(subject_list, demo_list, label_list, window_size, stride,
                     use_augmentation=False, graph_method='knn_shrinkage', k=10):
    graphs = []
    for sub_idx, (sub, dem, lab) in enumerate(zip(subject_list, demo_list, label_list)):
        n_rois, n_time = sub.shape
        if use_augmentation and n_time >= window_size:
            for start in range(0, n_time - window_size + 1, stride):
                end = start + window_size
                window = sub[:, start:end]
                w = np.hamming(window.shape[1])
                window_tapered = window * w[np.newaxis, :]
                try:
                    lw = LedoitWolf(assume_centered=False)
                    lw.fit(window_tapered.T)
                    cov = lw.covariance_
                except:
                    cov = np.cov(window_tapered.T, rowvar=False, bias=False)
                cov = np.nan_to_num(cov, nan=0.0, posinf=0.0, neginf=0.0)
                diag = np.clip(np.diag(cov).copy(), 0.0, None)
                d = np.sqrt(diag + 1e-6)
                corr = cov / np.outer(d, d)
                corr = np.nan_to_num(corr, nan=0.0, posinf=0.0, neginf=0.0)
                corr = np.clip(corr, -0.999, 0.999)
                np.fill_diagonal(corr, 1.0)
                corr_clip = np.clip(corr, -1+1e-3, 1-1e-3)
                fisher = 0.5 * np.log((1 + corr_clip) / (1 - corr_clip))
                fisher = np.nan_to_num(fisher, nan=0.0, posinf=3.0, neginf=-3.0)
                fisher = np.clip(fisher, -3.0, 3.0)
                adj = compute_graph_adj(fisher, method=graph_method, k=k)
                demo_exp = np.tile(dem.reshape(1, -1), (n_rois, 1))
                node_feat = np.concatenate((fisher, demo_exp), axis=1)
                node_feat = np.nan_to_num(node_feat, nan=0.0, posinf=0.0, neginf=0.0)
                x = torch.from_numpy(node_feat).float()
                edge_index, edge_attr = dense_to_sparse(torch.from_numpy(adj).float())
                edge_attr = torch.abs(edge_attr)
                edge_attr = torch.nan_to_num(edge_attr, nan=0.0, posinf=0.0, neginf=0.0)
                g = Data(x=x, edge_index=edge_index, edge_attr=edge_attr.float(),
                         y=torch.tensor([lab], dtype=torch.long))
                g.demographics = torch.from_numpy(dem).float().unsqueeze(0)
                g.num_nodes = n_rois
                g.adj_matrix = torch.from_numpy(adj).float()
                graphs.append(g)
        else:
            window = sub
            w = np.hamming(window.shape[1])
            window_tapered = window * w[np.newaxis, :]
            try:
                lw = LedoitWolf(assume_centered=False)
                lw.fit(window_tapered.T)
                cov = lw.covariance_
            except:
                cov = np.cov(window_tapered.T, rowvar=False, bias=False)
            cov = np.nan_to_num(cov, nan=0.0, posinf=0.0, neginf=0.0)
            diag = np.clip(np.diag(cov).copy(), 0.0, None)
            d = np.sqrt(diag + 1e-6)
            corr = cov / np.outer(d, d)
            corr = np.nan_to_num(corr, nan=0.0, posinf=0.0, neginf=0.0)
            corr = np.clip(corr, -0.999, 0.999)
            np.fill_diagonal(corr, 1.0)
            corr_clip = np.clip(corr, -1+1e-3, 1-1e-3)
            fisher = 0.5 * np.log((1 + corr_clip) / (1 - corr_clip))
            fisher = np.nan_to_num(fisher, nan=0.0, posinf=3.0, neginf=-3.0)
            fisher = np.clip(fisher, -3.0, 3.0)
            adj = compute_graph_adj(fisher, method=graph_method, k=k)
            demo_exp = np.tile(dem.reshape(1, -1), (n_rois, 1))
            node_feat = np.concatenate((fisher, demo_exp), axis=1)
            node_feat = np.nan_to_num(node_feat, nan=0.0, posinf=0.0, neginf=0.0)
            x = torch.from_numpy(node_feat).float()
            edge_index, edge_attr = dense_to_sparse(torch.from_numpy(adj).float())
