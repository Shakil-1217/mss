class IGA_Model(nn.Module):
    """IGA model with PCA feature injection and reduced capacity."""
    def __init__(self, in_channels, num_classes, n_rois, n_pca_components=20,
                 hidden=32, K=2, dropout=0.5, drop_edge_ratio=0.2):
        super().__init__()
        self.n_rois = n_rois
        self.n_pca = n_pca_components
        self.hidden = hidden
        self.dropout = dropout
        self.drop_edge_ratio = drop_edge_ratio

        # FIX: PCA feature projection layer (was missing entirely)
        self.pca_proj = nn.Linear(n_rois, hidden)

        # Hybrid component weighting (kept from original)
        self.mixing = nn.Parameter(torch.randn(in_channels, n_pca_components) * 0.01)
        self.selector = nn.Sequential(
            nn.Linear(n_pca_components, n_pca_components),
            nn.ReLU(),
            nn.Linear(n_pca_components, n_pca_components),
            nn.Sigmoid()
        )

        # 2 Cheb layers (removed comp_encoder to reduce params)
        self.conv1 = ChebConv(in_channels + hidden, hidden, K=K, normalization="sym")
        self.norm1 = nn.LayerNorm(hidden)

        # FIX: Residual connection for better gradient flow
        self.residual = nn.Linear(in_channels + hidden, hidden)

        self.conv2 = ChebConv(hidden, hidden, K=K, normalization="sym")
        self.norm2 = nn.LayerNorm(hidden)

        # Attention-based global pooling
        self.attn_pool = nn.Sequential(
            nn.Linear(hidden, hidden // 2),
            nn.Tanh(),
            nn.Linear(hidden // 2, 1)
        )

        self.classifier = nn.Sequential(
            nn.Linear(hidden, hidden // 2),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(hidden // 2, num_classes)
        )

    def drop_edge(self, edge_index, edge_attr, drop_ratio):
        if not self.training or drop_ratio == 0:
            return edge_index, edge_attr
        num_edges = edge_index.size(1)
        mask = torch.rand(num_edges, device=edge_index.device) > drop_ratio
        if edge_attr is not None:
            edge_attr = edge_attr[mask]
        return edge_index[:, mask], edge_attr

    def forward(self, data, pca_components):
        x = data.x
        edge_index = data.edge_index
        edge_attr = data.edge_attr if hasattr(data, "edge_attr") else None
        batch = data.batch

        # DropEdge regularization
        edge_index, edge_attr = self.drop_edge(edge_index, edge_attr, self.drop_edge_ratio)

        # ============================================================
        # FIX: Actually USE pca_components in forward (was passed but ignored)
        # ============================================================
        if pca_components is not None and len(pca_components) > 0:
            pca_stacked = torch.stack(pca_components, dim=0)  # (n_pca, N_ROIS, N_ROIS)
            pca_node_feat = pca_stacked.mean(dim=0)            # (N_ROIS, N_ROIS)
            pca_proj = self.pca_proj(pca_node_feat)            # (N_ROIS, hidden)
            pca_proj = F.dropout(pca_proj, p=self.dropout, training=self.training)
            # FIX: Repeat pca_proj for each graph in the batch
            batch_size = batch.max().item() + 1
            pca_proj = pca_proj.repeat(batch_size, 1)  # (batch * N_ROIS, hidden)
        else:
            pca_proj = torch.zeros(x.size(0), self.hidden, device=x.device)

        # Hybrid component weighting (kept from original)
        S = x @ self.mixing
        alpha = self.selector(S)
        weighted_x = x * alpha.mean(dim=1, keepdim=True)

        # Combine input with PCA projected features
        x_combined = torch.cat([x, pca_proj], dim=-1)

        # Cheb layer 1
        x1 = self.conv1(x_combined, edge_index, edge_attr)
        x1 = self.norm1(x1)
        x1 = F.relu(x1)
        x1 = F.dropout(x1, p=self.dropout, training=self.training)

        # FIX: Residual connection
        x1 = x1 + self.residual(x_combined)

        # Cheb layer 2
        x2 = self.conv2(x1, edge_index, edge_attr)
        x2 = self.norm2(x2)
        x2 = F.relu(x2)
        x2 = F.dropout(x2, p=self.dropout, training=self.training)

        # Attention-based global pooling
        attn_scores = self.attn_pool(x2).squeeze(-1)
        attn_weights = torch.sigmoid(attn_scores)
        graph_emb = global_mean_pool(x2 * attn_weights.unsqueeze(-1), batch)

        out = self.classifier(graph_emb)
        return out, graph_emb, x2, alpha


def decorrelation_loss(emb, eps=1e-5):
    if emb.size(0) < 2:
        return emb.sum() * 0.0
    emb_centered = emb - emb.mean(dim=0, keepdim=True)
    cov = torch.mm(emb_centered.T, emb_centered) / (emb.size(0) - 1 + eps)
    off_diag = cov - torch.diag(torch.diag(cov))
    loss = torch.norm(off_diag, p='fro') ** 2
    return loss


