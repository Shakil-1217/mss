# FIXED TRAINING: Mixup + Cosine Annealing + SWA
# ============================================================
def train_fold_iga(train_graphs, val_graphs, test_graphs, fold_id, online_pca):
    print(f"\n{'='*60}")
    print(f"Fold {fold_id}: Training IGA on {len(train_graphs)} graphs")
    print(f"{'='*60}")

    y_train = [g.y.item() for g in train_graphs]
    class_counts = np.bincount(y_train)
    total = len(y_train)
    weights_np = total / (NUM_CLASSES * np.maximum(class_counts, 1))
    class_weights = torch.tensor(weights_np ** 0.5, dtype=torch.float).to(device)
    print(f"Computed class weights: {class_weights.cpu().numpy()}")

    model = IGA_Model(
        in_channels=train_graphs[0].x.size(1),
        num_classes=NUM_CLASSES,
        n_rois=N_ROIS,
        n_pca_components=online_pca.n_components,
        hidden=HIDDEN_DIM,
        K=CHEBY_K,
        dropout=DROPOUT,
        drop_edge_ratio=0.2
    ).to(device)

    n_params = sum(p.numel() for p in model.parameters())
    print(f"Model parameters: {n_params:,}")

    optimizer = optim.AdamW(model.parameters(), lr=LEARNING_RATE,
                            weight_decay=WEIGHT_DECAY)

    # FIX: Cosine annealing with warm restarts (replaces ReduceLROnPlateau)
    scheduler = optim.lr_scheduler.CosineAnnealingWarmRestarts(
        optimizer, T_0=50, T_mult=2, eta_min=1e-6
    )

    train_loader = DataLoader(train_graphs, batch_size=BATCH_SIZE, shuffle=True)
    val_loader = DataLoader(val_graphs, batch_size=BATCH_SIZE, shuffle=False)
    test_loader = DataLoader(test_graphs, batch_size=BATCH_SIZE, shuffle=False)

    best_val_f1 = 0
    best_test_metrics = None
    patience_counter = 0

    # NEW: SWA setup
    swa_model = copy.deepcopy(model)
    swa_n = 0
    swa_active = False

    for epoch in range(1, NUM_EPOCHS + 1):
        model.train()
        total_loss = 0
        all_preds, all_labels = [], []

        for batch in train_loader:
            batch = batch.to(device)
            pca_U, pca_D = online_pca.get_components(n=online_pca.n_components)
            pca_comps = [
                torch.from_numpy(pca_U[:, i].reshape(N_ROIS, N_ROIS)).float().to(device)
                for i in range(pca_U.shape[1])
            ]

            # NEW: Mixup augmentation (output-level)
            if MIXUP_ALPHA > 0 and epoch > 5:
                lam, perm = mixup_data(batch.y, MIXUP_ALPHA)
                out, graph_emb, node_emb, alpha = model(batch, pca_comps)

                y_a, y_b = batch.y, batch.y[perm]
                loss = (lam * F.cross_entropy(out, y_a, weight=class_weights,
                        label_smoothing=LABEL_SMOOTHING)
                        + (1 - lam) * F.cross_entropy(out, y_b, weight=class_weights,
                        label_smoothing=LABEL_SMOOTHING))
            else:
                out, graph_emb, node_emb, alpha = model(batch, pca_comps)
                loss = F.cross_entropy(out, batch.y, weight=class_weights,
                                       label_smoothing=LABEL_SMOOTHING)

            # Decorrelation regularization
            loss_decorr = decorrelation_loss(graph_emb)
            loss = loss + DECorR_WEIGHT * loss_decorr

            optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=2.0)
            optimizer.step()

            total_loss += loss.item() * batch.num_graphs
            preds = out.argmax(dim=1)
            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(batch.y.cpu().numpy())

        train_acc = accuracy_score(all_labels, all_preds)
        train_loss = total_loss / len(train_graphs)

        scheduler.step()

        # NEW: SWA weight update
        if epoch >= SWA_START:
            if not swa_active:
                swa_active = True
                print(f"  -> SWA activated at epoch {epoch}")
            with torch.no_grad():
                swa_n += 1
                for p_swa, p_model in zip(swa_model.parameters(), model.parameters()):
                    p_swa.data = (p_swa.data * (swa_n - 1) + p_model.data) / swa_n
                for b_swa, b_model in zip(swa_model.buffers(), model.buffers()):
                    b_swa.data = b_model.data

        # Validation
        model.eval()
        val_preds, val_labels, val_probs = [], [], []
        with torch.no_grad():
            for batch in val_loader:
                batch = batch.to(device)
                pca_U, pca_D = online_pca.get_components(n=online_pca.n_components)
                pca_comps = [
                    torch.from_numpy(pca_U[:, i].reshape(N_ROIS, N_ROIS)).float().to(device)
                    for i in range(pca_U.shape[1])
                ]
                out, _, _, _ = model(batch, pca_comps)
                probs = F.softmax(out, dim=1)
                preds = probs.argmax(dim=1)
                val_preds.extend(preds.cpu().numpy())
                val_labels.extend(batch.y.cpu().numpy())
                val_probs.extend(probs[:, 1].cpu().numpy())

        val_acc = accuracy_score(val_labels, val_preds)
        val_f1 = f1_score(val_labels, val_preds, average='binary')

        # Evaluate SWA model on validation
        if swa_active:
            swa_model.eval()
            swa_preds, swa_labels = [], []
            with torch.no_grad():
                for batch in val_loader:
                    batch = batch.to(device)
                    pca_U, pca_D = online_pca.get_components(n=online_pca.n_components)
                    pca_comps = [
                        torch.from_numpy(pca_U[:, i].reshape(N_ROIS, N_ROIS)).float().to(device)
                        for i in range(pca_U.shape[1])
                    ]
                    out, _, _, _ = swa_model(batch, pca_comps)
                    preds = out.argmax(dim=1)
                    swa_preds.extend(preds.cpu().numpy())
                    swa_labels.extend(batch.y.cpu().numpy())
            swa_val_f1 = f1_score(swa_labels, swa_preds, average='binary')
            swa_val_acc = accuracy_score(swa_labels, swa_preds)
        else:
            swa_val_f1 = 0
            swa_val_acc = 0

        current_lr = optimizer.param_groups[0]['lr']

        if epoch % 10 == 0 or epoch == 1:
            swa_str = f" | SWA Val F1: {swa_val_f1:.4f}" if swa_active else ""
            print(f"Epoch {epoch:3d} | Train Loss: {train_loss:.4f} | "
                  f"Train Acc: {train_acc:.4f} | Val Acc: {val_acc:.4f} | "
                  f"Val F1: {val_f1:.4f} | LR: {current_lr:.6f}{swa_str}")

        # Use max of regular and SWA validation F1 for best model selection
        best_f1_this_epoch = max(val_f1, swa_val_f1)
        use_swa_for_test = swa_active and swa_val_f1 > val_f1

        if best_f1_this_epoch > best_val_f1:
            best_val_f1 = best_f1_this_epoch
            patience_counter = 0
            eval_model = swa_model if use_swa_for_test else model

            eval_model.eval()
            test_preds, test_labels, test_probs = [], [], []
            with torch.no_grad():
                for batch in test_loader:
                    batch = batch.to(device)
                    pca_U, pca_D = online_pca.get_components(n=online_pca.n_components)
                    pca_comps = [
                        torch.from_numpy(pca_U[:, i].reshape(N_ROIS, N_ROIS)).float().to(device)
                        for i in range(pca_U.shape[1])
                    ]
                    out, _, _, _ = eval_model(batch, pca_comps)
                    probs = F.softmax(out, dim=1)
                    preds = probs.argmax(dim=1)
                    test_preds.extend(preds.cpu().numpy())
                    test_labels.extend(batch.y.cpu().numpy())
                    test_probs.extend(probs[:, 1].cpu().numpy())
            test_acc = accuracy_score(test_labels, test_preds)
            test_f1 = f1_score(test_labels, test_preds, average='binary')
            test_auc = roc_auc_score(test_labels, test_probs) if len(set(test_labels)) > 1 else 0.5
            model_type = "SWA" if use_swa_for_test else "REG"
            print(f"  -> New best [{model_type}]! Test Acc: {test_acc:.4f} | "
                  f"F1: {test_f1:.4f} | AUC: {test_auc:.4f}")
            best_test_metrics = (test_acc, test_f1, test_auc)
        else:
            patience_counter += 1
            if patience_counter >= PATIENCE:
                print(f"Early stopping at epoch {epoch}")
                break

    # Final: try SWA model if not already used as best
    if swa_active and best_test_metrics:
        swa_model.eval()
        test_preds, test_labels, test_probs = [], [], []
        with torch.no_grad():
            for batch in test_loader:
                batch = batch.to(device)
                pca_U, pca_D = online_pca.get_components(n=online_pca.n_components)
                pca_comps = [
                    torch.from_numpy(pca_U[:, i].reshape(N_ROIS, N_ROIS)).float().to(device)
                    for i in range(pca_U.shape[1])
                ]
                out, _, _, _ = swa_model(batch, pca_comps)
                probs = F.softmax(out, dim=1)
                preds = probs.argmax(dim=1)
                test_preds.extend(preds.cpu().numpy())
                test_labels.extend(batch.y.cpu().numpy())
                test_probs.extend(probs[:, 1].cpu().numpy())
        swa_test_acc = accuracy_score(test_labels, test_preds)
        swa_test_f1 = f1_score(test_labels, test_preds, average='binary')
        swa_test_auc = roc_auc_score(test_labels, test_probs) if len(set(test_labels)) > 1 else 0.5
        print(f"  -> SWA final eval: Test Acc: {swa_test_acc:.4f} | "
              f"F1: {swa_test_f1:.4f} | AUC: {swa_test_auc:.4f}")
        if swa_test_f1 > best_test_metrics[1]:
            best_test_metrics = (swa_test_acc, swa_test_f1, swa_test_auc)

    return best_test_metrics


