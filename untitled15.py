class OnlineGraphPCA:
    """CCIPCA for streaming graph adjacency matrices."""
    def __init__(self, n_features, n_components=20, amnesic_factor=0):
        self.n_features = n_features
        self.n_components = n_components
        self.amnesic_factor = amnesic_factor
        self.n = 0
        self.mean = np.zeros(n_features)
        self.V = np.zeros((n_features, n_components))
        self.U = np.zeros((n_features, n_components))
        self.D = np.zeros(n_components)

    def update(self, x):
        self.n += 1
        if self.n == 1:
            self.V[:, 0] = x
            self.U[:, 0] = x / (np.linalg.norm(x) + 1e-10)
            self.D[0] = np.linalg.norm(x)
            return
        old_mean = self.mean.copy()
        self.mean = (self.n - 1) / self.n * old_mean + 1/self.n * x
        x_centered = x - old_mean
        x_rem = x_centered.copy()
        for j in range(self.n_components):
            if self.n > 1:
                norm_v = np.linalg.norm(self.V[:, j]) + 1e-10
                if self.amnesic_factor > 0:
                    w = (self.n - self.amnesic_factor) / (self.n + 1)
                else:
                    w = self.n / (self.n + 1)
                proj = x_rem.T @ (self.V[:, j] / norm_v)
                self.V[:, j] = w * self.V[:, j] + (1 - w) * proj * x_rem
                norm_new = np.linalg.norm(self.V[:, j]) + 1e-10
                self.U[:, j] = self.V[:, j] / norm_new
                self.D[j] = norm_new
                x_rem = x_rem - (x_rem.T @ self.U[:, j]) * self.U[:, j]
            else:
                self.V[:, j] = x_rem
                norm_v = np.linalg.norm(x_rem) + 1e-10
                self.U[:, j] = x_rem / norm_v
                self.D[j] = norm_v
                x_rem = x_rem - (x_rem.T @ self.U[:, j]) * self.U[:, j]

    def get_components(self, n=None):
        if n is None:
            n = self.n_components
        return self.U[:, :n], self.D[:n]

    def get_reconstruction(self, n=None):
        if n is None:
            n = self.n_components
        return self.U[:, :n] @ (self.D[:n, None] * self.U[:, :n].T)


