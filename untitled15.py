# NEW: Mixup helper for graph-level classification
# ============================================================
def mixup_data(labels, alpha=0.4):
    """Return lambda and permutation indices for Mixup."""
    if alpha > 0:
        lam = float(np.random.beta(alpha, alpha))
    else:
        lam = 1.0
    lam = max(lam, 1.0 - lam)
    batch_size = labels.size(0)
    perm = torch.randperm(batch_size, device=labels.device)
    return lam, perm


